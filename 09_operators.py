# Arithmetic operators
# Assignment operators
# Comparison operators
# Logical operators

# Python Arithmetic Operators
print(10 + 5)
print(10 - 5)
print(10 * 5)
print(10 / 5)
print(10 % 5)

# Python Assignment Operators try one by one
x = 5	
x += 3	
x -= 3
x *= 3
x /= 3
print(x)

# Python Comparison Operators this will return boolean value TRUE OR FALSE
x = 3
y = 4
print(x==y)
print(x!=y)
print(x<y)
print(x>y)
print(x<=y)
print(x>=y)

# Python Logical Operators
x = 3
y = 4
# and operator
print(x<y and x>2)
print(x<y and x<2)

# or operator
print(x<y or x>2)
print(x<y or x<2)

# not operator 
# this will return false if result is true
print(not(x < 5 and x < 10))